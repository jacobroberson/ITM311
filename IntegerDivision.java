public class IntegerDivision {
	
	public static void main(String args[]) {
	//Initialize variables
	int num1 = 3;
	float num2 = 7.65F;
	//display num1/num2 and num2/num1
	System.out.println(num1 / num2);
	System.out.println(num2 / num1);
	}
}